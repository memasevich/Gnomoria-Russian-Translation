def run():
    # 1. Загружаем сырой дамп
    raw_path = 'C:/Users/Lecoo/projects/GnomoriaTranslator/Gnomoria_raw_catch.json'
    with open(raw_path, 'r', encoding='utf-8', errors='ignore') as f:
        import re
        keys = re.findall(r'\"([^\"]+)\"\s*:', f.read())
    
    db = get_exhaustive_map()
    sorted_pats = sorted(db.keys(), key=len, reverse=True)
    
    final_dict = {}
    for k in set(keys):
        if not k.strip() or k == '?': continue
        
        val = k
        if k == "v1.0": 
            val = "v0.0.2 | memasevich 2026"
        else:
            # Сначала пытаемся найти точное совпадение
            if k in db:
                val = db[k]
            else:
                # Если не нашли, заменяем фрагментами
                for p in sorted_pats:
                    if p.lower() in val.lower():
                        # Используем точную замену (case-insensitive)
                        pattern = re.compile(re.escape(p), re.IGNORECASE)
                        def rep_f(match):
                            rus = db[p]
                            orig = match.group(0)
                            if orig.isupper(): return rus.upper()
                            if orig[0].isupper(): return rus.capitalize()
                            return rus
                        val = pattern.sub(rep_f, val)
        
        # Финальная чистка
        val = re.sub(r'\s+', ' ', val).strip()
        final_dict[k] = val

    # 2. Пишем в игру
    out_path = 'D:/steam/steamapps/common/Gnomoria/Gnomoria_en_ru.json'
    with open(out_path, 'w', encoding='utf-8') as f:
        json.dump(final_dict, f, ensure_ascii=False, indent=2)

    print(f"Translation applied: {len(final_dict)} entries processed.")

if __name__ == "__main__":
    run()
